Cypress.Commands.add('cleanFieldsValues', function() {
    const firstName = 'Bruno'
    const lastName = 'Garcia'
    const email = 'bruno@teste.com.br'
    const telefone = '11940702423'
    cy.get('#firstName')
      .type(firstName)
      .should('have.value', firstName)
      .clear()
      .should('have.value', '')
    cy.get('#lastName')
      .type(lastName)
      .should('have.value', lastName)
      .clear()
      .should('have.value', '')
    cy.get('#email')
      .type(email)
      .should('have.value', email)
      .clear()
      .should('have.value', '')
    cy.get('#phone-checkbox')
      .click()
    cy.get('#phone')
      .type(telefone)
      .should('have.value', telefone)
      .clear()
      .should('have.value', '')
})
