Cypress.Commands.add('fillMandatoryFieldsAndSubmit', function () {
    const textLong = 'Salve o tricolor paulista, amado clube brasileiro. Tu és forte tú és grande, dentre os grande és o primeiro'
    cy.get('#firstName')
        .should('be.visible')
        .type('Bruno')
    cy.get('#lastName')
        .should('be.visible')
        .type('Garcia')
    cy.get('#email')
        .should('be.visible')
        .type('bruno@teste.com.br')
    cy.get('#open-text-area')
        .should('be.visible')
        .type(textLong, { delay: 0 })
    cy.contains('button','Enviar').click()
})

